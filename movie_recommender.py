def createAccount():
    print("Welcome to Tulip! To get started, enter your information to create an account.")

    allowedDomains = (
            "@gmail.com",
            "@yahoo.com",
            "@outlook.com",
            "@hotmail.com",
            "@icloud.com"
    )

    while True:
        userEmail = input("\nEnter your email:\n").lower().strip()
        if userEmail.endswith(allowedDomains):
            break
        else:
            print("Invalid email address, please try again.")

    while True:
        userPassword = (input("Create a password, requires 8 - 15 character length.\n"))
        if len(userPassword) < 8:
            print("Password is not long enough, must be at least 8 characters long.")
        elif len(userPassword) > 15:
            print("Password too long, the limit is 15 characters.")
        else:
            break

    print("\nAccount created. Welcome to Tulip.\nLet's begin creating your profile!")

    userName = input("\nEnter your name:\n").capitalize().strip()
    userAge = int(input(f"Welcome {userName}! Enter your age:\n"))

    moviesAvailable = []

    print("\nMovies you can watch:")
    for rating, movieData in movie_dict.items(): # Loops/Iterates through keys and values inside movie_dict
        min_age, _ = movieData["ageRange"]
        if userAge >= min_age:
            for movie in movieData["movies"]: # Loops through each movie
                moviesAvailable.append(movie) # Adds each movie from rating to moviesAvailable depending on the user's age
                print(f"- {movie["title"]}")

    while True:
        genreInput = input("\nWhat genre are you looking for?\n").lower().strip()
        genreSearch = genre_aliases.get(genreInput, genreInput)

        found = False

        print(f"\nShowing movies matching '{genreSearch}':")

        for movie in moviesAvailable:
            movieGenres = [genre.lower() for genre in movie["genres"]]
            if genreSearch in movieGenres:
                genreDisplay = ", ".join(movie["genres"])
                print(f"- {movie["title"]} ({genreDisplay})")
                found = True

        if not found:
            print(f"No results found for '{genreSearch}'.")

if __name__ == "__main__":
    createAccount()